#include <iostream>
using namespace std;

int main()
{
    int a = 12, b = 5;

    cout << "sum of two numbers is:" << a + b << endl;
    cout << "substraction of two numbers is:" << a - b << endl;
    cout << "multiplication of two numbers is:" << a * b << endl;
    cout << "division of two numbers is:" << a / b << endl;
    cout << " remainder is:" << a % b;

    return 0;
}